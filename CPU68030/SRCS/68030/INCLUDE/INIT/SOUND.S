;****************************************************************
;		PrÇparation de l'Çcran.
;****************************************************************
Init_snd:	move		ccr,-(sp)
		movem.l		d0-d7/a0-a6,-(sp)


.Init_snd_end:	movem.l		(sp)+,d0-d7/a0-a6
		bset		#2,(1.w,sp)
		rtr
.Init_snd_err:	movem.l		(sp)+,d0-d7/a0-a6
		bclr		#2,(1.w,sp)
		rtr
;****************************************************************


;****************************************************************
;		Sauvegarde des paramätres du systäme sonore.
;****************************************************************
Save_snd:	movem.l		d0-d7/a0-a6,-(sp)

		movem.l		(sp)+,d0-d7/a0-a6
		rts
;****************************************************************


;****************************************************************
;		Restitution des paramätres du systäme sonore.
;****************************************************************
Restore_snd:	movem.l		d0-d7/a0-a6,-(sp)

		movem.l		(sp)+,d0-d7/a0-a6
		rts
;****************************************************************